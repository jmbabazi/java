public class Max {

    /**
     * @verson 1.1
     * @shows the maximum value
     * @param num1
     * @param num2
     * @return
     */
    public static int max(int num1, int num2) {
    int result;
    if (num1 > num2)
        result = num1;
    else
        result = num2;
    return result;
    }
    
}
